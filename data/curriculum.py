def get_track(track_id: int) -> dict:
    return TRACKS.get(track_id, {})


def get_lesson(track_id: int, lesson_id: int) -> dict:
    track = get_track(track_id)
    return track.get("lessons", {}).get(lesson_id, {})


def get_lesson_count(track_id: int) -> int:
    return len(TRACKS.get(track_id, {}).get("lessons", {}))


def is_last_lesson(track_id: int, lesson_id: int) -> bool:
    return lesson_id == get_lesson_count(track_id)


def get_next_lesson_id(track_id: int, lesson_id: int) -> int | None:
    if is_last_lesson(track_id, lesson_id):
        return None
    return lesson_id + 1
